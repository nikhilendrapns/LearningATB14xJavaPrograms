package ex_04_Operators;

public class Lab_045_Interview_Char_Int
{

    public static void main(String[] args) {

        char a1 = 'A'; //  ASCII -> 65
        char a2 = 'B'; //  ASCII -> 66
        System.out.println(a1+a2); // AB or 65+66 , it will print 131, because + operator is used for addition

        // char are noting but int :D
        //Character is converted to its ASCII value when used in arithmetic operations
        //So, 'A' is converted to 65 and 'B' is converted to 66, and then added together to give 131
        // Character are nothing but integer values in the memory


    }
}
